
namespace Epi
{
    /// <summary>
    /// Category for statistics level
    /// </summary>
    public enum StatisticsLevel
    {
        /// <summary>
        /// Output displays none of the statistics information
        /// </summary>
        None = 1,
        /// <summary>
        /// Output displays only minimal statistics information
        /// </summary>
        Minimal = 2,
        /// <summary>
        /// Output displays intermediate statistics information
        /// </summary>
        Intermediate = 3,
        /// <summary>
        /// Output displays advanced statistics information
        /// </summary>
        Advanced = 4
    }
}
