namespace Epi
{
    /// <summary>
    /// Short hand notation for booleans etc.
    /// </summary>
    public static class ShortHands
    {
        /// <summary>
        /// Yes value notation
        /// </summary>
		public const string YES = "(+)";
        /// <summary>
        /// No value notation
        /// </summary>
		public const string NO = "(-)";
        /// <summary>
        /// Missing value notation
        /// </summary>
		public const string MISSING = "(.)";
    }
}
